class PrintValues {
   public void print(int val) {
        System.out.println("The value is: " + val);
    }
   public void print(double val) {
        System.out.println("The value is: " + val);
    }
   public void print(char val) {
        System.out.println("The value is: " + val);
    }
    public void print(String val) {
        System.out.println("The value is: " + val);
    }
}